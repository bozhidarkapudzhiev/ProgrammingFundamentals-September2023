package _31_10_2023_AssociativeArrays.Comparator.Products;

public class Product {
        String name;
        double price;

    public Product(String name, double price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }
}
